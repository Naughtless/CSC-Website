function createImg(src, name) {
    var img = document.createElement("img")
    img.setAttribute("data-src", src)
    img.className = "owl-lazy"
    img.alt = name
    var imgContainer = document.createElement("div")
    imgContainer.className = "imgContainer"
    imgContainer.appendChild(img)
    return imgContainer
}

function createName(name) {
    var nama = document.createElement("h3")
    nama.innerText = nameShorter(name, 24)
    nama.className = "text-center mt-2"
    return nama
}

function createTitle(jabatan) {
    var title = document.createElement("p")
    title.innerText = jabatan
    return title
}

function createContainer() {
    var container = document.createElement("div")
    container.className = "item"
    return container
}

function createCell(name, role, img) {
    var container = createContainer()
    var imgDiv = createImg(img, name)
    var name = createName(name)
    var title = createTitle(role)

    name.appendChild(title)
    container.appendChild(imgDiv)
    container.appendChild(name)
    return container
}

function createCarousel(divisi) {
    var carousel = document.createElement("div")
    carousel.className = "owl-carousel owl-theme"
    divisi.forEach((value) => {
        carousel.appendChild(createCell(value.nama, value.jabatan, value.img))
    })
    return carousel
}

function initCarousel(member) {
    member.forEach((divisi) => {
        document.body.appendChild(createCarousel(divisi))
    })
}

function nameShorter(name, maxLen = 24) {
    var editedName = name
    var nameLen = name.length
    if (nameLen > maxLen) {
        editedName = editedName.split(" ")
        for (var x = editedName.length - 1; nameLen > maxLen; x--) {
            editedName[x] = editedName[x].substring(0, 1).toUpperCase() + "."
            nameLen = editedName.reduce((previousValue, currentValue) => {
                return previousValue + currentValue.length
            }, 0)
            nameLen += editedName.length
        }
        editedName = editedName.join(" ")
    }
    return editedName
}
